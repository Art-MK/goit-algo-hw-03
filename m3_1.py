# Вимоги до завдання:
# Функція приймає один параметр: date — рядок, що представляє дату у форматі 'РРРР-ММ-ДД' (наприклад, '2020-10-09').
# Функція повертає ціле число, яке вказує на кількість днів від заданої дати до поточної. Якщо задана дата пізніша за поточну, результат має бути від'ємним.
# У розрахунках необхідно враховувати лише дні, ігноруючи час (години, хвилини, секунди).
# Для роботи з датами слід використовувати модуль datetime Python.
# author Глінський Артем Валерійович

from datetime import datetime

def get_days_from_today(date) -> int:
    try:
        # Парсимо задану дату з рядка у об'єкт datetime
        input_date = datetime.strptime(date, '%Y-%m-%d').date()

        # Отримуємо поточну дату
        today_date = datetime.now().date()

        # Визначаємо різницю у днях
        delta = today_date - input_date

        # Повертаємо кількість днів у вигляді цілого числа
        return delta.days
    except:
        # Обробляємо помилку, якщо введено невірний формат дати
        return "Помилка: Невірний формат дати. Використовуйте формат 'YYYY-MM-DD'."

# Приклад використання:
print(get_days_from_today('2020-10-09')) # -> 1243
print(get_days_from_today('10-09-2020')) # -> Помилка: Невірний формат дати. Використовуйте формат 'YYYY-MM-DD'
